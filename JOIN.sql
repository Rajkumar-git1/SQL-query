USE BASIC;
CREATE TABLE ST (
ID INT PRIMARY KEY,
NAME VARCHAR(50));
INSERT INTO ST (ID,NAME) 
VALUES
(1001,"A"),
(1002,"B"),
(1003,"c");

CREATE TABLE CR ( -- COURCE
ID INT PRIMARY KEY,
SUB VARCHAR(50));
INSERT INTO CR (ID,SUB) 
VALUES
(1001,"ENG"),
(1002,"MATH"),
(1003,"SCIENCE");




-- INNER  JOIN
SELECT CR.ID, ST.NAME, CR.SUB 
FROM ST
INNER JOIN CR
ON ST.ID = CR.ID;

SELECT * FROM ST
INNER JOIN CR
ON ST.ID = CR.ID;

-- OUTER JOIN
-- 1- LEFT JOIN
SELECT s.ID, s.NAME, c.SUB
FROM ST AS s
LEFT JOIN CR AS c
ON s.ID = c.ID;

-- 2- RIGHT JOIN 
SELECT ST.ID , ST.NAME, CR.SUB 
FROM ST
RIGHT JOIN CR
ON ST.ID= CR.ID;

-- 3 - FULL JOIN
SELECT ST.ID , ST.NAME , CR.SUB
FROM ST
LEFT JOIN CR 
ON ST.ID = CR.ID
UNION
SELECT ST.ID , ST.NAME, CR.SUB
FROM ST
RIGHT JOIN CR 
ON ST.ID = CR.ID;

-- SELF JOIN
SELECT ST.ID, ST.NAME, CR.SUB
FROM ST
JOIN CR
ON ST.ID = CR.ID;


